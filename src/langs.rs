// 全局语言访问器 — 供 search.rs / audio.rs / config.rs 等模块使用
// ============================================================================

use std::sync::atomic::{AtomicU8, Ordering};

static GLOBAL_LANG_IDX: AtomicU8 = AtomicU8::new(0); // 0 = Sc

/// 设置全局语言（由 main.rs / ui.rs 在语言变更时调用）
pub fn set_global_language(lang: UiLanguage) {
    let idx = match lang {
        UiLanguage::Sc => 0u8,
        UiLanguage::Tc => 1,
        UiLanguage::En => 2,
        UiLanguage::Ja => 3,
        UiLanguage::Ko => 4,
        UiLanguage::Ru => 5,
        UiLanguage::Fr => 6,
        UiLanguage::De => 7,
        UiLanguage::Es => 8,
        UiLanguage::It => 9,
        UiLanguage::Pt => 10,
    };
    GLOBAL_LANG_IDX.store(idx, Ordering::Relaxed);
}

/// 获取当前全局语言的翻译文本（供非 UI 模块使用）
pub fn global_texts() -> &'static LangTexts {
    match GLOBAL_LANG_IDX.load(Ordering::Relaxed) {
        1 => &TC,
        2 => &EN,
        3 => &JA,
        4 => &KO,
        5 => &RU,
        6 => &FR,
        7 => &DE,
        8 => &ES,
        9 => &IT,
        10 => &PT,
        _ => &SC,
    }
}
