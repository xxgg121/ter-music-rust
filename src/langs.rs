// 语言包模块 — 集中管理所有界面翻译文本

/// 界面语言枚举
#[derive(Debug, Clone, Copy, PartialEq)]
pub enum UiLanguage {
    Sc,
    Tc,
    En,
    Ja,
    Ko,
    Ru,
    Fr,
    De,
    Es,
    It,
    Pt,
}

impl UiLanguage {
    pub fn next(self) -> Self {
        match self {
            UiLanguage::Sc => UiLanguage::Tc,
            UiLanguage::Tc => UiLanguage::En,
            UiLanguage::En => UiLanguage::Ja,
            UiLanguage::Ja => UiLanguage::Ko,
            UiLanguage::Ko => UiLanguage::Ru,
            UiLanguage::Ru => UiLanguage::Fr,
            UiLanguage::Fr => UiLanguage::De,
            UiLanguage::De => UiLanguage::Es,
            UiLanguage::Es => UiLanguage::It,
            UiLanguage::It => UiLanguage::Pt,
            UiLanguage::Pt => UiLanguage::Sc,
        }
    }

    pub fn config_key(self) -> &'static str {
        match self {
            UiLanguage::Sc => "sc",
            UiLanguage::Tc => "tc",
            UiLanguage::En => "en",
            UiLanguage::Ja => "ja",
            UiLanguage::Ko => "ko",
            UiLanguage::Ru => "ru",
            UiLanguage::Fr => "fr",
            UiLanguage::De => "de",
            UiLanguage::Es => "es",
            UiLanguage::It => "it",
            UiLanguage::Pt => "pt",
        }
    }

    pub fn from_config_key(s: &str) -> Self {
        if s.eq_ignore_ascii_case("sc") || s.eq_ignore_ascii_case("zh-cn") || s.eq_ignore_ascii_case("zh_hans") || s.eq_ignore_ascii_case("cn") || s == "简体" || s == "中文简体" {
            UiLanguage::Sc
        } else if s.eq_ignore_ascii_case("tc") || s.eq_ignore_ascii_case("zh-tw") || s.eq_ignore_ascii_case("zh_hant") || s.eq_ignore_ascii_case("tw") || s == "繁体" || s == "中文繁体" {
            UiLanguage::Tc
        } else if s.eq_ignore_ascii_case("en") || s.eq_ignore_ascii_case("english") {
            UiLanguage::En
        } else if s.eq_ignore_ascii_case("ja") || s.eq_ignore_ascii_case("jp") || s.eq_ignore_ascii_case("japanese") {
            UiLanguage::Ja
        } else if s.eq_ignore_ascii_case("ko") || s.eq_ignore_ascii_case("kr") || s.eq_ignore_ascii_case("korean") {
            UiLanguage::Ko
        } else if s.eq_ignore_ascii_case("ru") || s.eq_ignore_ascii_case("russian") || s == "русский" {
            UiLanguage::Ru
        } else if s.eq_ignore_ascii_case("fr") || s.eq_ignore_ascii_case("french") || s == "français" {
            UiLanguage::Fr
        } else if s.eq_ignore_ascii_case("de") || s.eq_ignore_ascii_case("german") || s == "deutsch" {
            UiLanguage::De
        } else if s.eq_ignore_ascii_case("es") || s.eq_ignore_ascii_case("spanish") || s == "español" {
            UiLanguage::Es
        } else if s.eq_ignore_ascii_case("it") || s.eq_ignore_ascii_case("italian") || s == "italiano" {
            UiLanguage::It
        } else if s.eq_ignore_ascii_case("pt") || s.eq_ignore_ascii_case("portuguese") || s == "português" {
            UiLanguage::Pt
        } else {
            UiLanguage::Sc
        }
    }

    #[allow(dead_code)]
    pub fn display_name(self) -> &'static str {
        match self {
            UiLanguage::Sc => "中文简体",
            UiLanguage::Tc => "中文繁體",
            UiLanguage::En => "English",
            UiLanguage::Ja => "日本語",
            UiLanguage::Ko => "한국어",
            UiLanguage::Ru => "Русский",
            UiLanguage::Fr => "Français",
            UiLanguage::De => "Deutsch",
            UiLanguage::Es => "Español",
            UiLanguage::It => "Italiano",
            UiLanguage::Pt => "Português",
        }
    }

    /// 获取当前语言的翻译文本
    pub fn texts(self) -> &'static LangTexts {
        match self {
            UiLanguage::Sc => &SC,
            UiLanguage::Tc => &TC,
            UiLanguage::En => &EN,
            UiLanguage::Ja => &JA,
            UiLanguage::Ko => &KO,
            UiLanguage::Ru => &RU,
            UiLanguage::Fr => &FR,
            UiLanguage::De => &DE,
            UiLanguage::Es => &ES,
            UiLanguage::It => &IT,
            UiLanguage::Pt => &PT,
        }
    }
}

